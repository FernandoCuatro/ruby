# Array de datos
videojuegos = ["zelda","mario"]

# Una opcion
# Para agregar a un array existente
videojuegos << "Super Smash"

# metodo de los arreglos
videojuegos.push("Mario Kart")

# Si quieremos agregar al inicio
videojuegos.unshift("Elemento 1")

# Para agregar dos elementos
# Podemos usar la figura que le estamos sumando otro arreglo
videojuegos += ["FIFA", "PES"]